<p>The Killers are an American rock band based in Las Vegas, Nevada. The band was formed in 2001 by Brandon Flowers and Dava Keuning, but the band would be completed by Mark Stoermer and Ronnie Vannucci, Jr. in 2002. The Killers are seen as one of the biggest rock bands of the 21st century, having sold an estimated 22 million records worldwide. They have performed in over 50 countries and on six continents, even headlining venues such as Madison Square Garden and Wembley Stadium.</p>
